const programmingLanguagesDataset = [];

function loadProgrammingLanguagesDataset() {
  const lines = programmingLanguagesRawDataset.split('\n');
  const columnNames = lines[0].split(',').map(x => x.trim());
  
  for (let i = 1;i < lines.length;i++) {
    const row = lines[i];
    const values = row.split(',').map(x => x.trim());
    let obj = {};

    for (let j = 0; j < columnNames.length;j++) {
      obj[columnNames[j]] = values[j];
    }

    programmingLanguagesDataset.push(obj);
  }
}

